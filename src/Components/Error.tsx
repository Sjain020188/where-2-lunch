/* -------------------------------------------------------------------------- */
/*                              Types                                          */
/* -------------------------------------------------------------------------- */

type Props = {
  msg: string;
};

/* -------------------------------------------------------------------------- */
/*                              Component                                     */
/* -------------------------------------------------------------------------- */

/**
 * This component is used to render error when API fails to fetch data.
 *
 */
export function Error({ msg }: Props) {
  return <p>{msg}</p>;
}
